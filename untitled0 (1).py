# 1. CLASE ESTUDIANTE
# ==========================================
class Estudiante:
    def __init__(self, nombre, id_estudiante, notas, activo=True):
        self.nombre = nombre
        self.id_estudiante = id_estudiante
        self.notas = notas  # Lista de 5 notas
        self.activo = activo

    def obtener_promedio(self):
        return sum(self.notas) / len(self.notas) if self.notas else 0

    def obtener_estado(self):
        return "Aprobado" if self.obtener_promedio() >= 3.0 else "Reprobado"

    def info(self):
        prom = self.obtener_promedio()
        estado = self.obtener_estado()
        return (f"ID: {self.id_estudiante} | Nombre: {self.nombre} | "
                f"Promedio: {prom:.2f} | Estado: {estado}")

