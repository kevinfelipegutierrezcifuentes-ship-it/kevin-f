        reprobados = len(promedios_lista) - aprobados

        print("\n" + "-"*40)
        print(" RESUMEN DEL GRUPO ".center(40))
        print("-"*40)
        print(f"Promedio General del Grupo: {prom_grupal:.2f}")
        print(f"Mejor Promedio Alcanzado:   {mejor_nota:.2f}")
        print(f"Total Aprobados:           {aprobados}")
