        print("2. Listar Estudiantes")
        print("3. Buscar Estudiante (ID o Nombre)")
        print("4. Actualizar Notas (por ID)")
        print("5. Eliminar Estudiante (ID o Nombre)")
        print("6. Ver Estadísticas del Grupo")
        print("7. Salir")

        opcion = input("\nSeleccione una opción: ")

        if opcion == "1":
            nom = input("Nombre: ")
            idx = input("Identificación: ")
            notas = []
            print("Ingrese las 5 notas (0.0 - 5.0):")
            while len(notas) < 5:
                try:
                    n = float(input(f"  Nota {len(notas)+1}: "))
                    if 0 <= n <= 5: notas.append(n)
                    else: print("  [!] Rango 0-5.")
                except ValueError: print("  [!] Ingrese números.")
            gestion.agregar(Estudiante(nom, idx, notas))

        elif opcion == "2":
            gestion.listar()

        elif opcion == "3":
            print("\n¿Cómo desea buscar?")
            print("1. Por ID")
            print("2. Por Nombre")
            sub_opcion = input("Elija (1/2): ")

            res = None
            if sub_opcion == '1':
                valor = input("Ingrese el ID: ")
                res = gestion.buscar_por_id(valor)
            elif sub_opcion == '2':
                valor = input("Ingrese el Nombre: ")
                res = gestion.buscar_por_nombre(valor)
            else:
                print("[!] Opción inválida.")
                continue

            print(f"\nResultado: {res.info()}" if res else "\n[!] Estudiante no encontrado.")

        elif opcion == "4":
            idx = input("Ingrese ID para actualizar: ")
            if gestion.buscar_por_id(idx):
                nuevas = []
                print("Ingrese las 5 nuevas notas:")
                while len(nuevas) < 5:
                    try:
                        n = float(input(f"  Nota {len(nuevas)+1}: "))
                        if 0 <= n <= 5: nuevas.append(n)
                        else: print("  [!] Rango 0-5.")
                    except ValueError: print("  [!] Ingrese números.")
                gestion.actualizar_notas(idx, nuevas)
                print("\n[OK] Notas actualizadas.")
            else:
                print("\n[!] Estudiante no encontrado.")

        elif opcion == "5":
            print("\n¿Cómo desea eliminar?")
            print("1. Por ID")
            print("2. Por Nombre")
            sub_opcion = input("Elija (1/2): ")

            exito = False
            if sub_opcion == '1':
                valor = input("Ingrese el ID: ")
                exito = gestion.eliminar_estudiante("id", valor)
            elif sub_opcion == '2':
                valor = input("Ingrese el Nombre: ")
                exito = gestion.eliminar_estudiante("nombre", valor)
            else:
                print("[!] Opción inválida.")
