# 2. GESTIÓN DE CRUD Y LÓGICA DE GRUPO
# ==========================================
class GestionEscolar:
    def __init__(self):
        self.estudiantes = []

    def agregar(self, estudiante):
        self.estudiantes.append(estudiante)
        print("\n[OK] Estudiante registrado con éxito.")

    def listar(self):
        if not self.estudiantes:
            print("\n[!] No hay estudiantes en el sistema.")
        else:
            print("\n--- LISTADO DE ESTUDIANTES ---")
            for e in self.estudiantes:
                print(e.info())

    def buscar_por_id(self, id_buscado):
        for e in self.estudiantes:
            if e.id_estudiante == id_buscado:
                return e
        return None

    def buscar_por_nombre(self, nombre_buscado):
        for e in self.estudiantes:
            if e.nombre.lower() == nombre_buscado.lower():
                return e
        return None

    def actualizar_notas(self, id_buscado, nuevas_notas):
        estudiante = self.buscar_por_id(id_buscado)
        if estudiante:
            estudiante.notas = nuevas_notas
            return True
        return False

    def eliminar_estudiante(self, criterio, valor):
        estudiante = None
        if criterio == "id":
            estudiante = self.buscar_por_id(valor)
        elif criterio == "nombre":
            estudiante = self.buscar_por_nombre(valor)

        if estudiante:
            self.estudiantes.remove(estudiante)
            return True
        return False

    def mostrar_reporte_estadistico(self):
        if not self.estudiantes:
            print("\n[!] No hay datos para generar estadísticas.")
            return

        print("\n" + "-"*40)
        print(" REPORTE INDIVIDUAL (NOMBRE Y PROMEDIO) ".center(40))
        print("-"*40)

        promedios_lista = []
        for e in self.estudiantes:
            p = e.obtener_promedio()
            promedios_lista.append(p)
            print(f"Estudiante: {e.nombre.ljust(15)} | Promedio: {p:.2f}")

